using Microsoft.Extensions.DependencyInjection;
using Experts.First_Project.HealthChecks;

namespace Experts.First_Project.Web.HealthCheck
{
    public static class AbpZeroHealthCheck
    {
        public static IHealthChecksBuilder AddAbpZeroHealthCheck(this IServiceCollection services)
        {
            var builder = services.AddHealthChecks();
            builder.AddCheck<First_ProjectDbContextHealthCheck>("Database Connection");
            builder.AddCheck<First_ProjectDbContextUsersHealthCheck>("Database Connection with user check");
            builder.AddCheck<CacheHealthCheck>("Cache");

            // add your custom health checks here
            // builder.AddCheck<MyCustomHealthCheck>("my health check");

            return builder;
        }
    }
}
